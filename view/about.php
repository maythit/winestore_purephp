                        <p>
                            A rose (from French rose), also labelled rosado in Portuguese and Spanish-speaking countries and rosado in Italy, is a type of wine that incorporates some of the color from the grape skins, but not enough to quality it as a red wine. It may be the oldest known type of wine, as it is the most straightforward to make with the skin contact method. The pink color can range from a pale “onion-skin” orange to a vivid near-purple, depending on the grape varieties used and winemaking techniques.</p><p>
                            There are three major ways to produce rose wine : skin contact, saignee and blending. Rose wines can be made still, semi-sparkling or sparkling and with a wide range of sweetness levels from highly dry Provencal rose to sweet White Zinfandels and blushes. Rose wines are made from a wide variety of grapes and can be found all around the globe.
                            When rose wine is the primary product, it is produced with the skin contact method. Black-skinned grapes are crushed and the skins are allowed to remain in contact with the juice for a short period, typically two to twenty hours. The must us then pressed, and the skins are discarded rather then left in contact throughout fermentation( as with red wine making). The longer the skins are left in contact with the juice the more intense the color of the final wine.                   
